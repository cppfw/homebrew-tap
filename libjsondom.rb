class Libjsondom < Formula
  desc "C++ cross-platform JSON DOM library."
  homepage "https://github.com/cppfw/jsondom"
  url "https://github.com/cppfw/jsondom/archive/0.1.12.tar.gz"
  sha256 "0e930b1366983f3eae54fb228beb06354b287d67427df68402fdb5420327b90f"

  depends_on "prorab" => :build
  depends_on "prorab-extra" => :build
  depends_on "myci" => :build
  depends_on "pkg-config" => :build
  depends_on "libclargs" => :build
  depends_on "libpapki"
  depends_on "libutki"

  def install
    ENV['PATH'] += ':/usr/local/bin'
    system "make", "install", "PREFIX=#{prefix}"
  end

  test do
    system "make", "test"
  end
end
